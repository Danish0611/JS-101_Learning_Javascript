let a = 10
let b = 4

console.log("a + b = ", a + b)
console.log("a - b = ", a - b)
console.log("a / b = ", a / b)
console.log("a ** b = ", a ** b)
console.log("a % b = ", a % b)
// a val = 10                                
console.log("++a = ", ++a)  // 1+10 = a val = 11
console.log("a++ = ", a++)  // 11+1 = a val = 12
console.log("--a = ", --a)  // 1-12 = a val = 11
console.log("a-- = ", a--)  // 11-1 = a val = 10
console.log("a = ", a)      // 10   = a val = 10
console.log("a-- = ", a--)  // 10-1 = a val = 12
